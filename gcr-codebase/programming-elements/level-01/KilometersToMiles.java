// Create KilometersToMiles Class to compute kilometer to mile.
public class KilometersToMiles{
	public static void main(String[] args){

	    // Create variable kilometerDistance for distance in miles.
	    double kilometerDistance = 10.8;
	    
	    // Create variable mileDistance for distance in kilometers.
	    double mileDistance = 10.8 * 1.6;
		
	    // Print the distance in kilometer and in mile distance.
	    System.out.println("The distance " +kilometerDistance+ "km in miles is " +mileDistance);
	}
}